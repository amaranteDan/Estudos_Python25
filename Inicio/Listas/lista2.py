# Iterar sobre os caracteres da primeira fruta e imprimir em minúsculas

frutas = ["maçã", "banana", "laranja", "uva"]
for char in frutas[0]:
    print(char.lower())
    
animais = ['cachorro', 'gato', 'coelho']
for i, char in enumerate(animais[1]): # Intera sobre os caracteres da primeira palavra
    print(f"Posição {i} : {char.upper()}")    
       
       
       
       